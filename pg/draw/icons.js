  function circle(cx, cy, r) {
    return 'M' + (cx - r) + ' ' + cy +
      'a' + r + ' ' + r + ' 0 1 0 ' + (r * 2) + ' 0' +
      'a' + r + ' ' + r + ' 0 1 0 ' + (-r * 2) + ' 0Z';
  }

