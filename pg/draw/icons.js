  function petals(cx, cy, r0, r1, n) {
    var d = '';
    for (var i = 0; i < n; i++) {
      var a = (Math.PI * 2 / n) * i - Math.PI / 2;
      var w = (Math.PI / n) * 0.78;                    // 꽃잎 반각
      var P = function (r, ang) {
        return (cx + r * Math.cos(ang)).toFixed(1) + ' ' + (cy + r * Math.sin(ang)).toFixed(1);
      };
      d += 'M' + P(r0, a - w) +
        'Q' + P(r1 * 1.02, a - w * 0.55) + ' ' + P(r1, a) +
        'Q' + P(r1 * 1.02, a + w * 0.55) + ' ' + P(r0, a + w) + 'Z';
    }
    return d;
  }

