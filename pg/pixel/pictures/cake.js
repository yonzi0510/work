/* 도안: 생일 케이크 32×32 — 어려움 단계 */
(() => {
  window.PIXELS = window.PIXELS || [];
  const W = 32, H = 32;
  // 1 배경, 2 접시, 3 아래층 분홍, 4 아래층 진분홍, 5 위층 민트, 6 위층 진민트,
  // 7 크림 흰색, 8 체리 빨강, 9 초 노랑, 10 촛불 주황
  const P = ['#FFEFF7', '#E3E8F5', '#FF9FBB', '#E87DA3', '#A8E8C0', '#7ED0A5', '#FFFFFF', '#FF4D6D', '#FFD93D', '#FF9D3A'];
  const g = Array.from({ length: H }, () => new Array(W).fill(0));

  // 접시
  for (let x = 3; x <= 28; x++) { g[27][x] = 1; g[28][x] = 1; }
  g[27][3] = 0; g[28][3] = 0; g[27][28] = 0; g[28][28] = 0;
  for (let x = 4; x <= 27; x++) g[29][x] = 1;

  // 아래층 (분홍, 오른쪽 그늘)
  for (let y = 20; y <= 26; y++)
    for (let x = 5; x <= 26; x++) g[y][x] = (x >= 24) ? 3 : 2;
  // 아래층 위 크림 물결 (아래로 흐르는 방울)
  for (let x = 5; x <= 26; x++) g[19][x] = 6;
  [6, 10, 14, 18, 22, 26].forEach(x => { if (x <= 26) g[20][x] = 6; });

  // 위층 (민트, 오른쪽 그늘)
  for (let y = 13; y <= 18; y++)
    for (let x = 9; x <= 22; x++) g[y][x] = (x >= 20) ? 5 : 4;
  // 위층 위 크림 물결
  for (let x = 9; x <= 22; x++) g[12][x] = 6;
  [10, 13, 16, 19, 22].forEach(x => { g[13][x] = 6; });

  // 체리: 아래층 선반 양쪽 + 위층 크림 위
  [6, 25].forEach(x => { g[18][x] = 7; });
  [10, 20].forEach(x => { g[11][x] = 7; });

  // 초 3개 + 촛불 (가운데 촛불은 크게)
  [12, 15, 18].forEach(x => {
    for (let y = 8; y <= 11; y++) g[y][x] = 8;
    g[7][x] = 9;
  });
  g[6][15] = 9;

  // 색종이 조각 (배경)
  [[3, 4, 7], [8, 2, 8], [24, 3, 7], [28, 6, 8], [2, 12, 8], [29, 14, 7], [4, 22, 8]].forEach(([x, y, c]) => {
    if (g[y][x] === 0) g[y][x] = c;
  });

  PIXELS.push({
    id: 'cake', name: '생일 케이크', emoji: '🎂', category: 'food', level: 3,
    palette: P,
    rows: g.map(r => r.join(''))
  });
})();
